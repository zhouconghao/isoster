"""S4G-like scenario mock adapter (same depth x redshift grid as Huang2013).

The S4G-like mocks are generated by the same libprofit-based
``generate_mocks.py`` as the Huang2013 set, with identical header
conventions. The only difference for the benchmark is ``dataset_name``
so output directories and inventory rows land under ``s4g/`` rather
than ``huang2013/``. All file-layout, ground-truth extraction, and
initial-geometry logic is inherited unchanged from
:class:`Huang2013ScenariosAdapter`.

Expected on-disk layout (configurable via the campaign YAML ``root``)::

    <root>/
      <galaxy_name>/
        <galaxy_name>_{clean|wide|deep}_z{005|020|035|050}.fits
"""

from __future__ import annotations

from .base import DatasetAdapter
from .huang2013_scenarios import Huang2013ScenariosAdapter


class S4GScenariosAdapter(Huang2013ScenariosAdapter):
    """S4G-like scenario mocks. Same layout / headers as Huang2013 scenarios."""

    dataset_name = "s4g"


ADAPTER_CLASS: type[DatasetAdapter] = S4GScenariosAdapter
